package org.wineShop.wine.web.dto;

public class TypeDTO {
	
	private Long id;
	private String name;
	
	
	public Long getId() {
		return id;
	}
	public void setId(Long categoryId) {
		this.id = categoryId;
	}
	public String getName() {
		return name;
	}
	public void setName(String categoryName) {
		this.name = categoryName;
	}
	
}
